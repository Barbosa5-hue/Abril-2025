dia = float(input("digite o númeo correspondente ao dia: "))
#Verificação dos dias da semana
if dia == 1:
    print("É Segunda-feira")
elif dia == 2:
    print("É Terça-feira")
elif dia == 3:
    print("É Quarta-feira")
elif dia == 4:
    print("É Quinta-feira")
elif dia == 5:
    print("É Sexta-feira")
elif dia == 6:
    print("É Sábado")
elif dia == 7:
    print("É Domingo")